    private void Tabla1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tabla1MouseClicked
        try {
            int fila = Tabla1.getSelectedRow();
            if(rs.absolute(fila+1)){
                CategoriaDProductos.TxCod.setText(rs.getString("codgru"));
                CategoriaDProductos.TxDes.setText(rs.getString("desgru"));
                CategoriaDProductos.TxCuenta.setText(rs.getString("codigoinv"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(TablaCatePro.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
                ser.desconectar(rs);
            } catch (SQLException ex) {
                Logger.getLogger(CategoriaDProductos.class.getName()).log(Level.SEVERE, null, ex);
            }
         try {
            this.setVisible(false);
            this.setClosed(true);
        } catch (PropertyVetoException ex) {
            Logger.getLogger(ProductoXBodega.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Tabla1MouseClicked

