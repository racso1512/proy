    private void JBGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBGrabarActionPerformed
        if(banderaInsert==0){
            if(TxCod.getText().isEmpty()){
                jLTx1.setText("El Campo de Codigo no puede quedar Vacio.");
                return;
            }
            if(TxDes.getText().isEmpty()){
                jLTx2.setText("Ingrese una descripción.");
                return;
            }
            if(TxCod.getText().length()>3){
                jLTx1.setText("El codigo introducido excede los 3 caracteres.");
                return;
            }
            try {
                ps=ser.Querys("INSERT INTO ingru (codgru,desgru,codigoinv)values(?,?,?)");
                ps.setString(1,TxCod.getText().toString());
                ps.setString(2,TxDes.getText().toString());
                ps.setString(3,TxCuenta.getText().toString());
                ser.InsBorrElim(ps);
                ps=ser.Querys("SELECT codgru,desgru,codigoinv FROM ingru ");
                datos = ser.Consultar(ps);                    
            } catch (SQLException ex) {
                Logger.getLogger(CategoriaDProductos.class.getName()).log(Level.SEVERE, null, ex);
                if(ex.getErrorCode()==1062){
                    Frame f = JOptionPane.getFrameForComponent(this);
                    CodigoExistente dialog = new CodigoExistente(f, true);
                    dialog.setVisible(true);
                    TxCod.setText("");
                    TxDes.setText("");
                    TxCuenta.setText("");  
                    jLTx1.setText("");
                    jLTx2.setText("");
                    return;
                }
            }
            banderaInsert=1;
            jBInicio.setEnabled(true);
            jBAnterior.setEnabled(true);
            jBProximo.setEnabled(true);
            jBUltimo.setEnabled(true);
            jBAdicionar.setEnabled(true);
            jBEliminar.setEnabled(true);
            jBModificar.setEnabled(true);
            jBBuscar.setEnabled(true);
            JBGrabar.setEnabled(false);
            JBCancelar.setEnabled(false);
            jLTx1.setText("");
            jLTx2.setText("");
        }
        if(banderaUpdate==0){
             if(TxDes.getText().isEmpty()){
                 jLTx2.setText("Ingrese una descripción.");
                 return;
             }
             try {
                ps=ser.Querys("UPDATE ingru SET desgru = ?, codigoinv = ? where codgru = ? ");
                ps.setString(1,TxDes.getText());
                ps.setString(2,TxCuenta.getText());
                ps.setString(3,TxCod.getText());
                ser.InsBorrElim(ps);
                ps=ser.Querys("SELECT codgru,desgru,codigoinv FROM ingru ");
                datos=ser.Consultar(ps);
                 try {
                    if(datos.next()){  
                        TxCod.setText(datos.getString("codgru"));
                        TxDes.setText(datos.getString("desgru"));
                        TxCuenta.setText(datos.getString("codigoinv"));
                        bandera1=0;
                
                    }
                    } catch (SQLException ex) {
                        Logger.getLogger(LineaDProductos.class.getName()).log(Level.SEVERE, null, ex);
                        }
                                
            } catch (SQLException ex) {
                Logger.getLogger(CategoriaDProductos.class.getName()).log(Level.SEVERE, null, ex);
                return;
            }
            banderaUpdate=1;
            jBInicio.setEnabled(true);
            jBAnterior.setEnabled(true);
            jBProximo.setEnabled(true);
            jBUltimo.setEnabled(true);
            jBAdicionar.setEnabled(true);
            jBEliminar.setEnabled(true);
            jBModificar.setEnabled(true);
            jBBuscar.setEnabled(true);
            JBGrabar.setEnabled(false);
            JBCancelar.setEnabled(false);
            TxCod.setEditable(true);
            jLTx2.setText("");
            
        }
    }//GEN-LAST:event_JBGrabarActionPerformed

