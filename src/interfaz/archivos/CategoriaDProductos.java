    public void centrarPantallaInternal(JInternalFrame ventana){
        int x =(PrinciPAL.DeskPanelP.getWidth()/2)-ventana.getWidth()/2;
        int y =(PrinciPAL.DeskPanelP.getHeight()/2)-ventana.getHeight()/2;
        if(ventana.isShowing()){
            ventana.setLocation(x, y);
        }else{
            PrinciPAL.DeskPanelP.add(ventana);
            ventana.setLocation(x, y);
            ventana.setVisible(true);
        } 
    };
    
    
    

